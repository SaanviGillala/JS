export function Add(number1, number2) {
    var result = number1 + number2;
    return result;
}

export function Subtract(number3, number4) {
    var result = number3 - number4;
    return result;
}

export function multiply(number5, number6) {
    var result = number5 * number6;
    return result;
}

export function divide(number7, number8) {
    var result = number7 / number8;
    return result;
}